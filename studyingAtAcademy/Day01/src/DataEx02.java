
public class DataEx02 {

	public static void main(String[] args) {
		//정답 : A,C,F
		byte var1=127;	// byte타입 최대값 범위를 벗어남
		short var2=128;
		int var3=28;	//28L은 long 타입에만 가능함 //값뒤에 접미사 L을 붙이면 long타입 변수에 저장해야 함.
		long var4=128L;
		float var5=123456.789123f; 	//float 변수는 뒤에f를 붙는다
		double var6=123456.789123;
	}
}
